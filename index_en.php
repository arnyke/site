            <u><h1>About us</h1></u>
        <br>
        <p>
          The goal of the international Children’s Safety Service is to assist and to improve the circumstances of life of the physically, intellectually, socially disadvantaged or orphan children as well as of those gifted. This organization works irrespectively of religions, political or national affiliation and is based on volunteering. 
In Romania, the I.C.S.S. was founded in 1991, taking as on example and being sponsored by the German and the Hungarian organizations.
Anybody, who needs support, can turn to our organization for help. Usually we are contracted by parents, relatives or teachers. Our organization doesn’t deal with cases of police, law court or real estate.
The presidency of our association decides on the award of grants and sponsoring. Our work is helped by many volunteers: doctors, lawyers, teachers, ardent pensioners and youngsters.
Of course, our Service couldn’t work without the aid of our sponsors: the “goods” to be shared out come from sponsorships, donations offered by companies and private individuals, income of charity programmers, project funds and from the professionalism and social work of our volunteering supporters.



